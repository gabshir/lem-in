#include "libft.h"

void	ft_putnbr(int n)
{
	int	decs;

	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n = -147483648;
		}
		n *= -1;
	}
	decs = 1;
	while (decs <= n / 10)
		decs *= 10;
	while (decs > 0)
	{
		ft_putchar((n / decs) + '0');
		n %= decs;
		decs /= 10;
	}
}
